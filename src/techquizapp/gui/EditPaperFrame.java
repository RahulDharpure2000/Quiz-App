    private void btnEditQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionsActionPerformed
        if(!validateInput()){
           JOptionPane.showMessageDialog(null, "Please Select a subject" , "no Selection",JOptionPane.ERROR_MESSAGE);
           return;
       }
       String subject=jcbSubjects.getSelectedItem().toString();
       String examId=jcbExamId.getSelectedItem().toString();
       int ans=JOptionPane.showConfirmDialog(null, "You Want to edit paper of "+subject+" "+examId);
       if(ans==JOptionPane.YES_OPTION){
           try{
               int totalQno=ExamDao.getQueCountByExamId(examId);
               System.out.println(examId+ ""+ totalQno);
               Exam exam=new Exam(examId, subject, totalQno);
               EditQuestionsFrame editQuestion=new EditQuestionsFrame(exam);
               editQuestion.setVisible(true);
               this.dispose();
           }
           catch (SQLException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, "Some problem occured t db EPF Error","EditPaper Error",JOptionPane.ERROR_MESSAGE);
        }
       
       }
       
    }//GEN-LAST:event_btnEditQuestionsActionPerformed

