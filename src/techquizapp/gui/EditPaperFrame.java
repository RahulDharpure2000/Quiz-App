    private void jcbSubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbSubjectsActionPerformed
       if(!validateInput()){
           JOptionPane.showMessageDialog(null, "Please Select a subject" , "Error",JOptionPane.ERROR_MESSAGE);
           return;
       }
        System.out.println("Subject name="+subjectname);
        try{
            ArrayList<String> idList=ExamDao.getExamIdBySubject(subjectname);
            System.out.println("idL" + idList);
            if(idList.isEmpty()){
                JOptionPane.showMessageDialog(null, "No exam id found for "+subjectname,"Exam not  found",JOptionPane.ERROR_MESSAGE);
                jcbSubjects.setSelectedIndex(0);
                return;
                
            }
            jcbExamId.removeAllItems();
            for(String id:idList)
                jcbExamId.addItem(id);
        } catch (SQLException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, "Some problem occured t db EPF Error","DB Error",JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_jcbSubjectsActionPerformed

