    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if(!validateInput()){
           JOptionPane.showMessageDialog(null, "Please fill all the input fields","Cannot Add Question!",JOptionPane.ERROR_MESSAGE);
           return;
        }
        String optionName=options.get(correctOption);
        Question ques=new Question(exam.getExamId(),qno , question, option1, option2, option3, option4, optionName,exam.getLanguage());
        System.out.println(ques);
        qstore.removeQuestion(pos);
        qstore.setQuestionAt(pos, ques);
        System.out.println(qstore);
        pos++;
        if(pos>=qstore.getCount()){
            JOptionPane.showMessageDialog(null, "Your question set has been successfully updated !. \n Press the DONE button to send it to the database","Exam Added!",JOptionPane.INFORMATION_MESSAGE  );
            System.out.println(qstore);
            pos=0;
        }
        showQuestion();
        qno++;
        if(qno>qstore.getCount())
           qno=1;
       lblQueNo.setText("Question No: "+qno);
        
    }//GEN-LAST:event_btnNextActionPerformed

