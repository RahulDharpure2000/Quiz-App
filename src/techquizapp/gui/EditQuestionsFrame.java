    private void loadQuestion() {
        try{
           
            ArrayList<Question> questionList=QuestionDao.getQuestionByExamId(exam.getExamId());
            System.out.println(questionList.size());
            for(Question ques:questionList){
                qstore.addQuestion(ques);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
             JOptionPane.showMessageDialog(null, "Some problem occured t db EPF Error","DB Error",JOptionPane.ERROR_MESSAGE);
    }
    }

    private void showQuestion() {
       Question q=qstore.getQuestion(pos);
       
       jtaQuestion.setText(q.getQuestion());
       txtOption1.setText(q.getAnswer1());
       txtOption2.setText(q.getAnswer2());
       txtOption3.setText(q.getAnswer3());
       txtOption4.setText(q.getAnswer4());
       String correctOpt=getKeyFromValue(q.getCorrectAnswer());
       System.out.println(correctOpt);
       jcbCorrectAnswer.setSelectedItem(correctOpt);
       
         
    }

    private String getKeyFromValue(String correctAnswer) {
       Set<String> keys=options.keySet();
       for(String key:keys){
           String currValue=options.get(key);
           if(currValue.equals(correctAnswer)){
               return key;
           }   
       }
       return null;
    }
    private boolean validateInput() {
        question=jtaQuestion.getText().trim();
        option1=txtOption1.getText().trim();
        option2=txtOption2.getText().trim();
        option3=txtOption3.getText().trim();
        option4=txtOption4.getText().trim();
        correctOption=jcbCorrectAnswer.getSelectedItem().toString();
        System.out.println("from validate "+correctOption);
        if(question.isEmpty() || option1.isEmpty() || option2.isEmpty() || option3.isEmpty() || option4.isEmpty())
            return false;
        return true;
        
        
    }
