    private void loadAllScores() {
        try{
            ArrayList<Performance> performanceList=PerformanceDao.getAllData();
            if(performanceList.isEmpty()){
                JOptionPane.showMessageDialog(null,"Sorry! No student has yet given any Exam","No Record Found",JOptionPane.ERROR_MESSAGE);
            }
            else{
                DefaultTableModel defaultTableModel=(DefaultTableModel)jtScore.getModel();
                Object[] rows=new Object[7];
                for(Performance performance:performanceList){
                    rows[0]=performance.getUserId();
                    rows[1]=performance.getExamId();
                    rows[2]=performance.getRight();
                    rows[3]=performance.getWrong();
                    rows[4]=performance.getUnAttempted();
                    rows[5]=performance.getSubject();
                    rows[6]=performance.getPercentage();
                    defaultTableModel.addRow(rows);
                }
                
            }
        }
          catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Db Exception occure","Db Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
