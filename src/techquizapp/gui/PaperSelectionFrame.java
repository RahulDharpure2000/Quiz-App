    private void btnTakeTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakeTestActionPerformed
        if(jcbSubjects.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Please Select a subject" , "no Selection",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(jcbExamId.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "Please Select a Exam" , "no Exam selected",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String examId=jcbExamId.getSelectedItem().toString();
        int ans=JOptionPane.showConfirmDialog(null, "You Want to Take test of "+subject+" "+examId);
        if(ans==JOptionPane.YES_OPTION){
            try{
                int totalQno=ExamDao.getQueCountByExamId(examId);
                System.out.println(examId+ ""+ totalQno +" "+subject);
                Exam exam=new Exam(examId, subject, totalQno);
                TestFrame takeTest=new TestFrame(exam);
                takeTest.setVisible(true);
                this.dispose();
            }
            catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Some problem occured in db EPF Error","PaperSelection Error",JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_btnTakeTestActionPerformed

