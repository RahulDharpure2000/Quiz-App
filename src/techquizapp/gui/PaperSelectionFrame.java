    private void jcbSubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbSubjectsActionPerformed
        jcbExamId.removeAllItems();
        if(jcbSubjects.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Please Select a subject" , "no Selection",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        subject=jcbSubjects.getSelectedItem().toString();
        System.out.println("Subject name="+subject);
    try {
        if(ExamDao.isPaperSet(subject)==false){
            JOptionPane.showMessageDialog(null, "No paper set for "+subject , "no Paper found",JOptionPane.ERROR_MESSAGE);
            jcbSubjects.setSelectedIndex(0);
            return;
        }
        ArrayList<String> idList=ExamDao.getExamIdBySubject(UserProfile.getUserName(),subject);
        System.out.println("idL" + idList);
        if(idList.isEmpty()){
            JOptionPane.showMessageDialog(null, "No exam  found for "+subject,"Exam not found",JOptionPane.ERROR_MESSAGE);
            jcbSubjects.setSelectedIndex(0);
            return;
            
        }
        for(String id:idList)
            jcbExamId.addItem(id);
        } 
    catch (SQLException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null, "Some problem occured in db SPF Error","DB Error",JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jcbSubjectsActionPerformed

