private boolean validateInputs() {
        studentId=txtUserId.getText();
        char[] pwd=jpfPassword.getPassword();
        char[] cpwd=jpfConfirmPassword.getPassword();
        if(studentId.isEmpty() || pwd.length==0 ||cpwd.length==0)
            return false;
        Password=new String(pwd);
        CPassword=new String(cpwd);
        return true;
    }
