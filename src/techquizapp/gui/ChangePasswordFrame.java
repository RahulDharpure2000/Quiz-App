    private void btnChangePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePasswordActionPerformed
      boolean isValidInput=validateInputs();
      
        if(isValidInput==false){
            JOptionPane.showMessageDialog(null, "Username or password cannot be left blank!","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String UserType=getUserType();
        if(UserType==null)
        {
            JOptionPane.showMessageDialog(null, "Please login with correct UserType","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(! (Password.equalsIgnoreCase(CPassword)))
        {
           
            JOptionPane.showMessageDialog(null, "Please Enter same password in both Fields","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        try{
            User user=new User();
            user.setUserId(studentId);
            user.setPassword(Password);
            System.out.println("uname "+studentId+" ,pwd "+Password+" ,user "+UserType);
            boolean ischangepassworld=UserDao.changePassword(user);
            System.out.println("frame"+ischangepassworld);
            if(ischangepassworld){
                JOptionPane.showMessageDialog(null, "Password Changed Successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
                
              }
                 
                 jpfPassword.setText("");
                 jpfConfirmPassword.setText("");
            
        } 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error","Changepassword Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } 
     
    }//GEN-LAST:event_btnChangePasswordActionPerformed

