    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInput()==false){
            JOptionPane.showMessageDialog(null, "Please fill all fields","Empty Fields Found",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!validatePassword()){
            JOptionPane.showMessageDialog(null, "Password and confim password should be same","Passwords not match",JOptionPane.ERROR_MESSAGE);
            
            return;
        }
        User user=new User(userId, password, "Student");
        System.out.println(user);
        try{
            boolean result=UserDao.registerStudent(user);
            if(result){
                JOptionPane.showMessageDialog(null, "Student Registered Successfully","Registration Done",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
                
            else
               JOptionPane.showMessageDialog(null, "Some Problem occured please try later hint-"+result,"Registration Failed",JOptionPane.ERROR_MESSAGE);
        
        }
        catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error at db occured","DB Error",JOptionPane.ERROR_MESSAGE);
    }
        
       
    }//GEN-LAST:event_btnRegisterActionPerformed

