    private boolean validateInput() {
        userId=txtUserId.getText();
        password=new String(jpfPassword.getPassword());
        confirmPssword=new String(jpfConfirmPassword.getPassword());
        System.out.println("id "+userId+" pwd "+password+" cnf "+confirmPssword);
        if(userId.isEmpty() || password.isEmpty() || confirmPssword.isEmpty())
            return false;
        return true;
    }

    private boolean validatePassword() {
        System.out.println("pwd "+password+",cnf "+confirmPssword);
        System.out.println(password.equals(confirmPssword));
        return password.equals(confirmPssword);
    }

